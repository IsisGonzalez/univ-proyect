        <p>A partir de allí se comenzó a relacionar el término Grand Slam con el éxito y su uso se expandió a otros deportes, como 
        béisbol, golf o rugby, y entre los exploradores para la persona que lograba llegar a los dos polos del planeta y a las siete 
        cumbres más altas de cada uno de los continentes.</p>
        
        <br></br>

        <p>El tenis es un deporte con vasta  trayectoria, que se viene desarrollando hace muchos años, en el cual la forma de enseñar 
        ha sido basada de acuerdo a como   se estructuró este deporte, o sea, de una forma muy reglamentada y  técnica. Es por esto 
        que esta investigación tiene la idea de describir y  justificar la importancia de la metodología basada en el juego en la 
        iniciación  del tenis  especialmente en los Juegos de Grand Slam.</p>
        
        <br></br>

